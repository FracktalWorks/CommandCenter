export interface ChatMessage {
  role: "user" | "assistant" | "system";
  content: string;
}

interface ChatRequest {
  agentName: string;
  message: string;
  messages: ChatMessage[];
  threadId: string;
  mode?: "langgraph" | "copilot" | "litellm";
  /** Copilot SDK model override forwarded to gateway (e.g. "claude-sonnet-4.5"). */
  model?: string;
  /** System-level context (persistent memory / persona) injected as context. */
  context?: string;
  /** Thinking mode: "auto" | "thinking" | "max" */
  thinkMode?: string;
  /** Assistant message ID generated by the frontend.  Used so server-side
   *  persistence writes to the SAME row the frontend renders, enabling
   *  reliable refresh recovery. */
  assistantMessageId?: string;
  /** Last SSE event ID the client received before disconnecting.  Sent on
   *  reconnect so the server can replay only missed events. */
  lastEventId?: string;
  /** When true, the client is reconnecting to an existing agent run rather
   *  than starting a new one.  The route calls the reconnect endpoint. */
  reconnect?: boolean;
}

